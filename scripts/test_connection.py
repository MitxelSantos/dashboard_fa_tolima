def test_postgresql_connection():
    """Prueba la conexi√≥n a PostgreSQL usando configuraci√≥n centralizada"""
    print("üêò Probando conexi√≥n a PostgreSQL...")
    
    try:
        # 1. Probar conexi√≥n b√°sica usando config centralizada
        engine = create_engine(DATABASE_URL)
        
        with engine.connect() as conn:
            print("‚úÖ Conexi√≥n exitosa usando configuraci√≥n centralizada!")
            print(f"   Host: {DatabaseConfig.HOST}:{DatabaseConfig.PORT}")
            print(f"   Base de datos: {DatabaseConfig.DATABASE}")
            print(f"   Usuario: {DatabaseConfig.USER}")
            
            # 2. Verificar extensiones
            print("\nüîß Extensiones instaladas:")
            try:
                extensiones = pd.read_sql(text("""
                    SELECT extname, extversion 
                    FROM pg_extension 
                    WHERE extname IN ('postgis', 'pg_trgm', 'unaccent', 'uuid-ossp')
                    ORDER BY extname
                """), conn)
                
                if len(extensiones) > 0:
                    for _, ext in extensiones.iterrows():
                        print(f"   ‚úÖ {ext['extname']} v{ext['extversion']}")
                else:
                    print("   ‚ö†Ô∏è No se encontraron extensiones esperadas")
            except Exception as e:
                print(f"   ‚ö†Ô∏è Error verificando extensiones: {e}")
            
            # 3. Verificar tablas creadas
            print("\nüìä Tablas del sistema:")
            try:
                tablas = pd.read_sql(text("""
                    SELECT table_name, table_type 
                    FROM information_schema.tables 
                    WHERE table_schema = 'public' 
                    AND table_type = 'BASE TABLE'
                    AND table_name IN (
                        'unidades_territoriales', 'poblacion', 'vacunacion_fiebre_amarilla',
                        'casos_fiebre_amarilla', 'epizootias'
                    )
                    ORDER BY table_name
                """), conn)
                
                if len(tablas) > 0:
                    for _, tabla in tablas.iterrows():
                        # Contar registros
                        try:
                            count = conn.execute(text(f"SELECT COUNT(*) FROM {tabla['table_name']}")).scalar()
                            print(f"   ‚úÖ {tabla['table_name']}: {count:,} registros")
                        except:
                            print(f"   üìã {tabla['table_name']}: tabla creada (sin datos)")
                else:
                    print("   ‚ö†Ô∏è Tablas del sistema no encontradas (normal en instalaci√≥n nueva)")
            except Exception as e:
                print(f"   ‚ö†Ô∏è Error verificando tablas: {e}")
            
            # 4. Verificar vistas cr√≠ticas
            print("\nüëÅÔ∏è Vistas del sistema:")
            try:
                vistas = pd.read_sql(text("""
                    SELECT table_name 
                    FROM information_schema.views 
                    WHERE table_schema = 'public'
                    AND table_name LIKE 'v_%'
                    ORDER BY table_name
                """), conn)
                
                if len(vistas) > 0:
                    for _, vista in vistas.iterrows():
                        try:
                            count = conn.execute(text(f"SELECT COUNT(*) FROM {vista['table_name']}")).scalar()
                            print(f"   ‚úÖ {vista['table_name']}: {count:,} registros")
                        except:
                            print(f"   üìã {vista['table_name']}: vista creada")
                else:
                    print("   ‚ö†Ô∏è Vistas del sistema no encontradas (normal en instalaci√≥n nueva)")
            except Exception as e:
                print(f"   ‚ö†Ô∏è Error verificando vistas: {e}")
        
        return True
        
    except Exception as e:
        print(f"‚ùå Error de conexi√≥n: {e}")
        print("\nüí° Posibles soluciones:")
        print("1. Verificar que Docker est√© corriendo: docker ps")
